# A set is an unodered,unindexed and mutable collection of unique items.
#Removing duplicate items and set operations(Union,Intersection,etc)

set1={1,2,3,4,5}
set2={5,6,7,8,9}
print("First Set : ",set1)
print("Second Set : ",set2)
print("Intersection : ",set1.intersection(set2))
print("Union : ",set1.union(set2))
print("Difference : ",set1.difference(set2))