def plus(a,b):
    total=a+b
    print(f"Summation of {a} and {b} : ",total)
def minus(a,b):
    total=a-b
    print(f"Substraction of {a} and {b} : ",total)
def mul(a,b):
    total=a*b
    print(f"Multiplication of {a} and {b} : ",total)
def div(a,b):
    total=a/b
    print(f"Division of {a} and {b} : ",total)
    
if __name__=="main":
    plus()
    minus()
    mul()
    div()